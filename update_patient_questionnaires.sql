CREATE TABLE IF NOT EXISTS patient_questionnaires (
  id INT AUTO_INCREMENT PRIMARY KEY,
  patient_name VARCHAR(100),
  age INT,
  gender VARCHAR(10),
  dominant_hand VARCHAR(10),
  injury_side VARCHAR(10),
  phone_number VARCHAR(20),
  sport_lifestyle VARCHAR(20),
  sport_level VARCHAR(50),
  sport_type VARCHAR(50),
  symptoms TEXT,
  medical_history TEXT,
  pain_level INT,
  pain_frequency VARCHAR(20),
  stronger_pain_killer VARCHAR(10),
  pain_killer_frequency INT,
  pain_killer_dosage INT,
  shoulder_pain VARCHAR(20),
  shoulder_pain_lifting VARCHAR(20),
  shoulder_satisfaction VARCHAR(50),
  shoulder_comfort VARCHAR(10),
  pain_or_spasm INT,
  weakness INT,
  fatigue INT,
  popping INT,
  stiffness INT,
  neck_discomfort INT,
  instability INT,
  compensation INT,
  protect_arm INT,
  fall_concern INT,
  problem_awareness INT,
  worsen_concern INT,
  emotional_impact INT,
  daily_work INT,
  routine_sports INT,
  leisure_activities INT,
  social_impact INT,
  work_limitation VARCHAR(50),
  wear_coat INT,
  wash_back INT,
  use_toilet INT,
  comb_hair INT,
  reach_high_shelf INT,
  put_coin_on_shelf INT,
  lift_weight INT,
  throw_ball_low INT,
  throw_ball_high INT,
  reach_behind INT,
  open_bottle VARCHAR(50),
  heavy_housework VARCHAR(50),
  carry_bags VARCHAR(50),
  cut_food VARCHAR(50),
  night_pain VARCHAR(10),
  side_sleepping VARCHAR(10),
  sleep_difficulty INT,
  created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
